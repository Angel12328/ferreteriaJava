    public void mostrarElemento(Proveedor _proveedor) {
        this.txtCodigo.setText(Integer.toString(_proveedor.getCodigoProveedor()));
        this.txtTelefono.setText(_proveedor.getTelefono());
        this.txtEmpresa.setText(_proveedor.getEmpresa());

    }

    public void actualizarElementosTabla() {

        String[] titulo = {"Código", "Empresa", "Teléfono"};

        DefaultTableModel dt = new DefaultTableModel(MDIPrincipal.gProveedor.getArrayGestion(), titulo);
        this.jtProveedores.setModel(dt);
    }

