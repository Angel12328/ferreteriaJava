    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Paso 1
        String codigo;
        String telefono;
        String empresa;

        codigo = this.txtCodigo.getText();
        telefono = this.txtTelefono.getText();
        empresa = this.txtEmpresa.getText();

        //PAso 2
        Proveedor proveedor = new Proveedor();
        try {
            //Paso 3
            proveedor.setCodigoProveedor(Integer.parseInt(codigo));
            proveedor.setTelefono(telefono);
            proveedor.setEmpresa(empresa);

            if (this._agregando) {
                //Nuevo Cliente
                /*solicitar valores a usuario
                crear instancia
                setear los valores a la instancia
                agregar instancia
                mostrar valores
                */

                //paso 4
                MDIPrincipal.gProveedor.agregarProveedor(proveedor);

            } else { //Editando Proveedor
                /*solicitar valores a usuario
                crear instancia modificada
                setear los valores a la instancia modificada
                modificar la instancia
                mostrar valores modificados
                */

                //Paso 4
                MDIPrincipal.gProveedor.modificarProveedor(proveedor, this._indiceSeleccionado);

            }

            //paso 5
            actualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Favor ingresar correctamente el codigo");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + e.getMessage());
        }
        //paso 6: serializar informacion
        AdminSerializacion.serializacion(MDIPrincipal.gProveedor, "gProveedor.obj");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Metodo para eliminar el objeto guardado
        if (_indiceSeleccionado >= 0) {
            int resultado = JOptionPane.showConfirmDialog(null, "Estas seguro?", "Warning", JOptionPane.YES_NO_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {
                MDIPrincipal.gProveedor.eliminarProveedor(_indiceSeleccionado);
                actualizarElementosTabla();

                //paso 6: serializar informacion
                AdminSerializacion.serializacion(MDIPrincipal.gProveedor, "gProveedor.obj");

            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor seleccione un elemento de la tabla que desea eliminar.");
        }
        estadoControles(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String _codigoProveedor = JOptionPane.showInputDialog("Ingrese el codigo del Proveedor");

        Proveedor _proveedorEncontrado = MDIPrincipal.gProveedor.buscar(Integer.parseInt(_codigoProveedor));
        if (_proveedorEncontrado == null) {
            JOptionPane.showMessageDialog(this, "Proveedor no encontrado");
        } else {
            mostrarElemento(_proveedorEncontrado);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

