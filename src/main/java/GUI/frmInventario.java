    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        // los pasos 1, 2, 3 y 5 son comunes
        //Paso 1
        String codigo;
        String producto;
        String precio;
        String stock;

        codigo = this.txtCodigo.getText();
        producto = this.txtProducto.getText();
        precio = this.txtPrecio.getText();
        stock = this.txtStock.getText();

        //PAso 2
        Inventario inventario = new Inventario();

        try {

            //Paso 3
            inventario.setCodigoInventario(Integer.parseInt(codigo));
            inventario.setNombreProducto(producto);
            inventario.setPrecio(Double.parseDouble(precio));
            inventario.setStock(Integer.parseInt(stock));
            inventario.setProveedor(_proveedor);

            if (this._agregando) {
                //Nueva funcion
                /*solicitar valores a usuario
                crear instancia
                setear los valores a la instancia
                agregar instancia
                mostrar valores
                */

                //paso 4
                MDIPrincipal.gInventario.agregarInventario(inventario);
                AdminSerializacion.serializacion(MDIPrincipal.arbolInventario, "gInventarioArbol.obj");

            } else {//Editando Funcion
                /*solicitar valores a usuario
                crear instancia modificada
                setear los valores a la instancia modificada
                modificar la instancia
                mostrar valores modificados
                */

                //Paso 4

                MDIPrincipal.gInventario.modificarInventario(inventario, this._indiceSeleccionado); //ENTRAR A LA FUNCION Y INGENNAIRSELA PARA METER EL ARBOL AHI Y MODIFICAAR
                AdminSerializacion.serializacion(MDIPrincipal.arbolInventario, "gInventarioArbol.obj");
            }

            //paso 5
            actualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Favor ingresar correctamente el codigo");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + e.getMessage());
        }

        //paso 6: serializar informacion
        AdminSerializacion.serializacion(MDIPrincipal.gInventario, "gInventario.obj");
        //AdminSerializacion.serializacion(MDIPrincipal.arbolInventario, "gInventarioArbol.obj");
    }//GEN-LAST:event_btnGuardarActionPerformed

