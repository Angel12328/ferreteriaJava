    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Metodo para eliminar el objeto guardado
        if (_indiceSeleccionado >= 0) {
            int resultado = JOptionPane.showConfirmDialog(null, "Estas seguro?", "Warning", JOptionPane.YES_NO_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {
                //elimina registro de LkL
                MDIPrincipal.gInventario.eliminarInventario(_indiceSeleccionado);
                //actualizarElementosTabla();
                //paso 6: serializar informacion
                AdminSerializacion.serializacion(MDIPrincipal.gInventario, "gInventario.obj");

                //Se deja nula la instancia
                MDIPrincipal.arbolInventario=null;
                MDIPrincipal.arbolInventario= new ArbolBinario();
                //borra el arbol
                AdminSerializacion.eliminarArchivoSerializado("gInventarioArbol.obj");

                //lo vuelve a rellenar sin el dato que se borro
                for (int i = 0; i < MDIPrincipal.gInventario.getInventarios().size(); i++) {
                    MDIPrincipal.gInventario.agregarInventario(MDIPrincipal.gInventario.getInventarios().get(i));
                }
                //serializa el nuevo arbol
                AdminSerializacion.serializacion(MDIPrincipal.arbolInventario, "gInventarioArbol.obj");
                MDIPrincipal.arbolInventario.mostrarInOrden(MDIPrincipal.arbolInventario.getRaiz());

                //actualiza la tabla en la interfaz
                actualizarElementosTabla();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor seleccione un elemento de la tabla que desea eliminar.");
        }
        estadoControles(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

