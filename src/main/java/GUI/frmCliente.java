    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String _codigoCliente = JOptionPane.showInputDialog("Ingrese el codigo del Cliente");

        Cliente _clienteEncontrado = MDIPrincipal.gCliente.buscar(Integer.parseInt(_codigoCliente));
        if (_clienteEncontrado == null) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado");
        } else {
            mostrarElemento(_clienteEncontrado);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Metodo para eliminar el objeto guardado
        if (_indiceSeleccionado >= 0) {
            int resultado = JOptionPane.showConfirmDialog(null, "Estas seguro?", "Warning", JOptionPane.YES_NO_OPTION);
            if (resultado == JOptionPane.YES_OPTION) {
                MDIPrincipal.gCliente.eliminarCliente(_indiceSeleccionado);
                actualizarElementosTabla();

                //paso 6: serializar informacion
                AdminSerializacion.serializacion(MDIPrincipal.gCliente, "gCliente.obj");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor seleccione un elemento de la tabla que desea eliminar.");
        }
        estadoControles(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

