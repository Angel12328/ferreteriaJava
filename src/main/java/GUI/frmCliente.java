    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // los pasos 1, 2, 3 y 5 son comunes
        //Paso 1
        String codigo;
        String nombre;
        String telefono;

        codigo = this.txtCodigo.getText();
        nombre = this.txtNombre.getText();
        telefono = this.txtTelefono.getText();

        //PAso 2
        Cliente cliente = new Cliente();
        try {
            //Paso 3
            cliente.setCodigo(Integer.parseInt(codigo));
            cliente.setNombre(nombre);
            cliente.setTelefono(telefono);

            if (this._agregando) { //Nuevo Cliente
                /*solicitar valores a usuario
            crear instancia
            setear los valores a la instancia
            agregar instancia
            mostrar valores
                 */

                //paso 4
                MDIPrincipal.gCliente.agregarCliente(cliente);

            } else { //Editando Cliente
                /*solicitar valores a usuario
            crear instancia modificada
            setear los valores a la instancia modificada
            modificar la instancia 
            mostrar valores modificados
                 */

                //Paso 4
                MDIPrincipal.gCliente.modificarCliente(cliente, this._indiceSeleccionado);

            }

            //paso 5
            actualizarElementosTabla();
            estadoControles(false);
            estadoBotones(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Favor ingresar correctamente el codigo ");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error: " + e.getMessage());
        }
        
        //paso 6: serializar informacion
        AdminSerializacion.serializacion(MDIPrincipal.gCliente, "gCliente.obj");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jtClientesMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClientesMousePressed
        _indiceSeleccionado = this.jtClientes.getSelectedRow();
        if (_indiceSeleccionado != -1) {
            Cliente _cliente = MDIPrincipal.gCliente.getClienteByIndex(_indiceSeleccionado);
            mostrarElemento(_cliente);

        }
    }//GEN-LAST:event_jtClientesMousePressed

