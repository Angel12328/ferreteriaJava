    /*Este metodo muestra los elemento en pantalla
    segun el objeto enviado*/
    public void mostrarElemento(Cliente _cliente) {
        this.txtCodigo.setText(Integer.toString(_cliente.getCodigo()));
        this.txtNombre.setText(_cliente.getNombre());
        this.txtTelefono.setText(_cliente.getTelefono());

    }

    public void actualizarElementosTabla() {

        String[] titulo = {"Codigo", "Nombre", "Telefono"};

        DefaultTableModel dt = new DefaultTableModel(MDIPrincipal.gCliente.getArrayGestion(), titulo);
        this.jtClientes.setModel(dt);
    }

    public void estadoControles(boolean _estado) {
        this.txtCodigo.setEditable(_estado);
        this.txtNombre.setEditable(_estado);
        this.txtTelefono.setEditable(_estado);

    }

    public void estadoBotones(boolean _estado) {

        this.btnBuscar.setEnabled(_estado);
        this.btnModificar.setEnabled(_estado);
        this.btnEliminar.setEnabled(_estado);
        this.btnGuardar.setEnabled(!_estado);
        this.btnNuevo.setEnabled(_estado);
    }

